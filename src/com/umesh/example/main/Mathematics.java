package com.umesh.example.main;

public class Mathematics {

	private int a = 0;
	private int b = 0;

	public Mathematics(int a, int b) {
		this.a = a;
		this.b = b;
	}

	public int sum() {

		return a + b;

	}

	public int sub() {
		return a - b;

	}

	public int mul() {
		return a * b;

	}

}
